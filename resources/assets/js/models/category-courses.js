class CategoryCourses {
    constructor(data) {
       
        for (let property in data) {
            this[property] = data[property];
        }

    }
    static source(){
        return '/category-courses'
    }
    static createUrl(){
         return this.source() + '/create' 
    }
    static storeUrl(){
         return this.source()
    }
    static showUrl(id){
         return this.source() + '/' + id
    }
    static editUrl(id){
         return this.showUrl(id) +  '/edit'
    }
    static updateUrl(id){
        return this.showUrl(id)
    }
    static deleteUrl(id){
         return this.source() + '/' + id
    }
    static index(category,center){
        return new Promise((resolve, reject) => {
            let url = '/category-courses'
            let params={
                category:category,
                center:center
            }
           let query=Helper.buildQuery(url,params)
            axios.get(query)
                .then(response => {
                   resolve(response.data)
                })
                .catch(error=> {
                     reject(error);
                })
           
        })
    }
    static create(category){
        return new Promise((resolve, reject) => {
            let url = '/category-courses'
            url+= '/create?category=' + category
            axios.get(url)
                .then(response => {
                   resolve(response.data)
                })
                .catch(error=> {
                     reject(error);
                })
           
        })
    }
    static store(category,courses){
        let url =this.storeUrl() 
        let method='post'
        let form=new Form({
            category:category,
            courses:courses
        })
        return new Promise((resolve, reject) => {
            form.submit(method,url)
                .then(data => {
                    resolve(data);
                })
                .catch(error => {
                    reject(error);
                })
        })
    }
    static show(id){
        return new Promise((resolve, reject) => {
            let url = this.showUrl(id) 
            axios.get(url)
                .then(response => {
                   resolve(response.data)
                })
                .catch(error=> {
                     reject(error);
                })
           
        })
    }
    static edit(id){
        let url = this.editUrl(id) 
        return new Promise((resolve, reject) => {
            axios.get(url)
                .then(response => {
                   resolve(response.data)
                })
                .catch(error=> {
                     reject(error);
                })
           
        })
    }
    static update(form , id){
         let url =this.updateUrl(id)
         let method='put'
        return new Promise((resolve, reject) => {
            form.submit(method,url)
                .then(data => {
                    resolve(data);
                })
                .catch(error => {
                    reject(error);
                })
        })
    }
    
    static delete(category,course) {
        return new Promise((resolve, reject) => {
            let url =this.deleteUrl(category) 
            url += '?course=' +  course
            let form = new Form()
            form.delete(url)
                .then(response => {
                    resolve(true);
                })
                .catch(error => {
                    reject(error);
                })
        })
    }
   
    

    
   
    

}


export default CategoryCourses;